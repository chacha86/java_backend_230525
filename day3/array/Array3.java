package day3.array;

public class Array3 {
    public static void main(String[] args) {
        // 배열을 두번 출력해주세요.
        int[] arr = {1,2,3,4,5};

        for(int j = 0; j < 2; j++) {
            for(int i = 0; i < 5; i++) {
                System.out.println(arr[i]);
            }
        }
    }
}
